package com.gateway.merchantmgr.model;
/**
 *
 */
public class MccInfo {
	private String id;
	private String mccDescribe;
	private String mccValue;
	private String type;
	
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getMccDescribe() {
		return mccDescribe;
	}
	public void setMccDescribe(String mccDescribe) {
		this.mccDescribe = mccDescribe;
	}
	public String getMccValue() {
		return mccValue;
	}
	public void setMccValue(String mccValue) {
		this.mccValue = mccValue;
	}
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	
}
