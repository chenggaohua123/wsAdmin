package com.gateway.form.model;

public class TransTimeCountInfo {
	private int hour;
	private String hourStr;
	private int count;
	public int getCount() {
		return count;
	}
	public void setCount(int count) {
		this.count = count;
	}
	public int getHour() {
		return hour;
	}
	public void setHour(int hour) {
		this.hour = hour;
	}
	public String getHourStr() {
		return hourStr;
	}
	public void setHourStr(String hourStr) {
		this.hourStr = hourStr;
	}
	
	
}
